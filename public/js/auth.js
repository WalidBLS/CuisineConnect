document.getElementById('testCreateUser').addEventListener('click', function(e) {
    e.preventDefault();

    fetch('/sign-up', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' }
    })
    .then(response => response.json())
    .then(data => {
        console.log(data)
    })
    .catch(error => {
        console.error('Erreur:', error);
    });
});
